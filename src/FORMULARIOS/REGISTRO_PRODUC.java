  //este metodo nos servira para poder mostrar los datos de la base de datos y verlos en una tabla asi verlos todos  
    public void mostrar (){
DefaultTableModel TABLA = new  DefaultTableModel();
TABLA.addColumn("ID_PRODUCTO");
TABLA.addColumn("PRODUCTO");
TABLA.addColumn("TIPO");
TABLA.addColumn("EMPRESA");
TABLA.addColumn("PRECIO");
TABLA.addColumn("ORIGEN");

TABLA_PRODUCTOS.setModel(TABLA);

String[]datos = new String[8];
try{
    Statement leer = conect.createStatement();
    ResultSet resultado = leer.executeQuery("SELECT*FROM PRODUCTO");
    while(resultado.next()){
    datos[0]=resultado.getString(1);
    datos[1]=resultado.getString(2);
    datos[2]=resultado.getString(3);
    datos[3]=resultado.getString(4);
    datos[4]=resultado.getString(5);
    datos[5]=resultado.getString(6);
    TABLA.addRow(datos);
            }
    TABLA_PRODUCTOS.setModel(TABLA);
}catch(SQLException e){
JOptionPane.showMessageDialog(null,e+" error en la consulta") ;
}
}

