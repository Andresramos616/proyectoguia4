public void eliminar_producto(){
int fila= TABLA_PRODUCTOS.getSelectedRow();
 String valor = (String) TABLA_PRODUCTOS.getValueAt(fila,0);
 try{
     PreparedStatement elimi= conect.prepareStatement("DELETE FROM producto WHERE ID_PRODUC='"+valor+"'");
     elimi.executeUpdate();
      JOptionPane.showMessageDialog(null,"SE ELIMINO CON EXITO :V") ;
 }catch(SQLException e){
     JOptionPane.showMessageDialog(null,e+" no se logro eliminar");
 }
 mostrar();      

}
