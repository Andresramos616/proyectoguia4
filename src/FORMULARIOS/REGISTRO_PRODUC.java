    //con este metodo podremos cojer los datos que estan en el sistema modificarlos y guardarlos nuevamente
    //usando de referencia el id del producto que es quien lo identifica
    public void ACTUALIZAR(){
    int fila= TABLA_PRODUCTOS.getSelectedRow();
    String id = (String) TABLA_PRODUCTOS.getValueAt(fila,0);
    String PRODUC= TABLA_PRODUCTOS.getValueAt(fila,1).toString();
    String TIP= TABLA_PRODUCTOS.getValueAt(fila,2).toString();
    String EMPRES= TABLA_PRODUCTOS.getValueAt(fila,3).toString();
   String PRECI= TABLA_PRODUCTOS.getValueAt(fila,4).toString();
    String ORIGE= TABLA_PRODUCTOS.getValueAt(fila,5).toString();
    
    try{
        PreparedStatement actu = conect.prepareStatement("UPDATE producto SET id_produc='"+id+"',producto='"+PRODUC+"',tipo='"+TIP+"',empresa='"+EMPRES+"',precio='"+PRECI+"',lugar_fabri='"+ORIGE+"' WHERE id_produc='"+id+"'");
        actu.executeUpdate();
        JOptionPane.showMessageDialog(null,"SE ACTUALIZO :3") ;
    }catch(SQLException e){JOptionPane.showMessageDialog(null,e+" no se logro actualizar") ;} 
    mostrar();          

    }
    
